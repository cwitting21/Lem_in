#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t len)
{
	char	*s;

	s = s1;
	while (*s != '\0')
		s++;
	while (len-- > 0 && *s2 != '\0')
		*s++ = *s2++;
	*s = '\0';
	return (s1);
}
